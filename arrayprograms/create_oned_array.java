package arrayprograms;

public class create_oned_array {
    public static void main(String[] args) {
        int[] arr = {36 , 78 , 98 , 28};
        System.out.println("The array elements are :");;

        for (int i = 0 ; i < arr.length ; i++)
        {
            System.out.println(arr[i]);;
        }
    }
}